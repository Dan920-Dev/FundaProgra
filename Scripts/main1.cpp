#include <iostream>

using namespace std;


// Funcion principal del programa
int main() {

    // Declaracion de variables necesarias
    int num;

    // Uso de cout - Salida de datos
    cout << "Hola, Ingrese un numero: " << endl; // endl hace un salto de linea

    // Uso de cin - Entrada de datos
    cin >> num; // Obtenemos el numero que el usuario ingrese

    // Hacemos una salida de datos con cout y mostramos el numero
    cout << "El numero ingresado es: " << num << endl; 

    return 0;
}